#[derive(Debug)]
pub struct State {
    pub locations: [u8; 8],
    pub last_moved: usize,
    pub score: usize,
}

impl State {
    pub fn is_done(&self) -> bool {
        let a = &self.locations[0..2];
        let b = &self.locations[2..4];
        let c = &self.locations[4..6];
        let d = &self.locations[6..8];
        (a == [0, 1] || a == [1, 0])
            && (b == [2, 3] || b == [3, 2])
            && (c == [4, 5] || c == [5, 4])
            && (d == [6, 7] || d == [7, 6])
    }
}

impl From<&str> for State {
    fn from(value: &str) -> State {
        fn loc_to_id((r, c): (usize, usize)) -> u8 {
            match (r, c) {
                (3, 3) => 0,
                (2, 3) => 1,
                (3, 5) => 2,
                (2, 5) => 3,
                (3, 7) => 4,
                (2, 7) => 5,
                (3, 9) => 6,
                (2, 9) => 7,
                _ => panic!(),
            }
        }
        let input = value
            .lines()
            .map(|line| line.chars().collect::<Vec<_>>())
            .collect::<Vec<_>>();
        let (mut a2, mut b2, mut c2, mut d2) = (false, false, false, false);
        let mut locations = [0; 8];
        for (r, c) in [
            (2, 3),
            (2, 5),
            (2, 7),
            (2, 9),
            (3, 3),
            (3, 5),
            (3, 7),
            (3, 9),
        ] {
            match input[r][c] {
                'A' if !a2 => {
                    locations[0] = loc_to_id((r, c));
                    a2 = true;
                }
                'A' if a2 => locations[1] = loc_to_id((r, c)),
                'B' if !b2 => {
                    locations[2] = loc_to_id((r, c));
                    b2 = true;
                }
                'B' if b2 => locations[3] = loc_to_id((r, c)),
                'C' if !c2 => {
                    locations[4] = loc_to_id((r, c));
                    c2 = true;
                }
                'C' if c2 => locations[5] = loc_to_id((r, c)),
                'D' if !d2 => {
                    locations[6] = loc_to_id((r, c));
                    d2 = true;
                }
                'D' if d2 => locations[7] = loc_to_id((r, c)),
                _ => panic!(),
            }
        }
        State {
            locations,
            last_moved: 10,
            score: 0,
        }
    }
}

