use crate::parsing::parse_input;

pub fn process(_input: &str) -> usize {
    let input = parse_input(_input);
    let mut cycle = 0;
    let mut target_cycle = 20;
    let mut x = 1;
    let mut ans = 0;
    for op in input {
        cycle += 1;
        if cycle == target_cycle {
            ans += cycle * x;
            target_cycle += 40;
        }
        if let Some(v) = op {
            cycle += 1;
            if cycle == target_cycle {
                ans += cycle * x;
                target_cycle += 40;
            }
            x += v;
        }
    }
    ans as usize
}

